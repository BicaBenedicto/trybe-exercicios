function cestaDeCompras (cesta) {
  cesta = basket;
  let cestaItems = {};
  let output2 = [];
  let output = "Sua cesta possui: ";

  for (let key of cesta){
    if (cestaItems[key]) {
      cestaItems[key] += 1;
    } else {
      cestaItems[key] = 1;
    }
  }
  for (let i in cestaItems) {
    if (cestaItems[i]) {
      output2.push(cestaItems[i]);
      output2.push(' ');
      output2.push([i]);
      output2.push('s, ');
    }
  }
  return output + output2.join('');
}